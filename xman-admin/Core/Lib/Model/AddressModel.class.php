class AddressModel extends Model {
    
    public function getAddress($where = '',$field = '*'){
        $add = $this->field($field)->where($where)->select();
        return $add ? $add : array();
    }
    
    public function getAddr($where = '',$field = '*'){
        return $this->field($field)->where($where)->find();
    }
}